CALL IASIZE('PPCLIM',IDBIA1)

!===============================================================================
! 2. AIGUILLAGE VERS LE MODELE ADEQUAT
!===============================================================================

! ---> Combustion gaz
!      Flamme de diffusion chimie 3 points

