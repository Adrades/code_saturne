      DOUBLE PRECISION TP    , DELT  , SOMPHI, VAL
      DOUBLE PRECISION RHO   , R     , CPA   , CPE , CPV , DEL
      DOUBLE PRECISION HV0 , HVTI , RHOJ , TTI , XXI,  XSATI , DXSATI
      DOUBLE PRECISION RHO0 , T00 , P00 , T1
